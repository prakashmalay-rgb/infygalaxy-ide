#!/usr/bin/env node
// generate-icon.js — Creates a placeholder icon for development
// For production, replace assets/icon.ico with your real branded icon

const fs = require('fs');
const path = require('path');

// Minimal valid 1x1 ICO file (for dev — replace with real icon for production)
const minimalIco = Buffer.from([
  0x00,0x00,0x01,0x00,0x01,0x00,0x01,0x01,0x00,0x00,0x01,0x00,0x18,0x00,
  0x30,0x00,0x00,0x00,0x16,0x00,0x00,0x00,0x28,0x00,0x00,0x00,0x01,0x00,
  0x00,0x00,0x02,0x00,0x00,0x00,0x01,0x00,0x18,0x00,0x00,0x00,0x00,0x00,
  0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
  0x00,0x00,0x00,0x00,0x6c,0x63,0xff,0x00,0x00,0x00
]);

const assetsDir = path.join(__dirname, 'assets');
if(!fs.existsSync(assetsDir)) fs.mkdirSync(assetsDir);

const icoPath = path.join(assetsDir,'icon.ico');
if(!fs.existsSync(icoPath)){
  fs.writeFileSync(icoPath, minimalIco);
  console.log('✓ Placeholder icon.ico created in assets/');
  console.log('  Replace with your real 256x256 icon for production.');
} else {
  console.log('✓ icon.ico already exists');
}
